// Helper to simulate live updates
export function simulateLiveUpdate(type: string) {
  if (type === 'signal') {
    return {
      type: 'paper_signal',
      data: {
        market_id: mockMarkets[Math.floor(Math.random() * mockMarkets.length)].market_id,
        decision: ['BUY', 'SELL', 'HOLD'][Math.floor(Math.random() * 3)],
        confidence: 0.5 + Math.random() * 0.4,
        edge: (Math.random() - 0.5) * 0.1,
      },
      timestamp: new Date().toISOString(),
    };
  }
  return null;
}
